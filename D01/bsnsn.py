# band name generator  generator
# this is a simple program that generates a band name based on the user's input
# it takes the user's input for the city they grew up in and their pet's name
# then it combines them to create a band name
# it is a fun way to create a unique band name based on personal information
print("Welcome to the Band Name Generator!")
city_name = input("Enter the city you grew up in: \n")
pet_name = input("Enter the name of your pet: \n")
band_name = city_name + " " + pet_name
print("Your band name could be: " + band_name)  

